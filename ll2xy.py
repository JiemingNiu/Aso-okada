###############################################
###############################################
###############################################
def ll2xy(slat,slon):
	sxx, syy = pForward(slon, slat)
	return (sxx, syy)
###############################################
###############################################
###############################################
def xy2ll(sxx,syy):
	slon, slat = pForward(sxx, syy, inverse=True)
	return (slat, slon)
