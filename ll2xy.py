#!/usr/bin/env python
import numpy as np
from pyproj import Proj
###############################################
###############################################
###############################################
# use Trasverse Mercator Projection 
# Evenden, G. I. libproj4: A Comprehensive Library of Cartographic Projection Functions (Preliminary Draft). 2005. URL: https://github.com/OSGeo/PROJ/blob/master/docs/old/libproj.pdf.
crater1st = [32.8849556,131.0851333]
pForward = Proj(proj='tmerc', ellps='WGS84', lat_0=crater1st[0], lon_0=crater1st[1], k_0=1, x_0=0.0, y_0=0.0) 
###############################################
###############################################
###############################################
